    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        Pessoa pessoa = new Pessoa();
        PessoaDao pessoaDao = new PessoaDao();

        pessoa.setNome(txtNome.getText());
        int tam = pessoaDao.pesquisaPessoa(pessoa).size();
        int id = pessoaDao.pesquisaPessoa(pessoa).get(0).getId();

        for (int i = 0; i < tam; i++) {
            txtNome.setText(pessoaDao.pesquisaPessoa(pessoa).get(i).getNome());
            txtEmail.setText(pessoaDao.pesquisaPessoa(pessoa).get(i).getEmail());
            jftTelefone.setText(pessoaDao.pesquisaPessoa(pessoa).get(i).getTelefone());
            id = pessoaDao.pesquisaPessoa(pessoa).get(i).getId();
        }
        Endereco endereco = new Endereco();
        EnderecoDao enderecoDao = new EnderecoDao();

        endereco.setId(id);
        for (int i = 0; i < enderecoDao.pesquisaEndereco(endereco).size(); i++) {
            txtRua.setText(enderecoDao.pesquisaEndereco(endereco).get(i).getRua());
            txtNumero.setText("" + enderecoDao.pesquisaEndereco(endereco).get(i).getNum());
        }

    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Pessoa pessoa = new Pessoa();
        PessoaDao pessoaDao = new PessoaDao();

        pessoa.setNome(txtNome.getText());
        pessoa.setEmail(txtEmail.getText());
        pessoa.setTelefone(jftTelefone.getText());
        pessoaDao.atualizarPessoa(pessoa);

        Endereco endereco = new Endereco();
        EnderecoDao enderecoDao = new EnderecoDao();

        pessoa.setNome(txtNome.getText());
        int id = pessoaDao.pesquisaPessoa(pessoa).get(0).getId();

        endereco.setId(id);
        endereco.setRua(txtRua.getText());
        endereco.setNum(Integer.parseInt(txtNumero.getText()));
        enderecoDao.atualizarEndereco(endereco);
    }//GEN-LAST:event_btnAtualizarActionPerformed

